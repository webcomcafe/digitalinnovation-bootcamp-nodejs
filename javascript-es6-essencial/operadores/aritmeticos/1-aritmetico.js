// Modelo (%)
// Operador binário. Retorna o inteiro restante da divisão dos dois operandos.

12 % 5; //Retorna 2

// Incremento (++) e Decremento (--)
++x;
x++;

--x;
x--;

// Negação (-) e adição (+)
-3;
+"3";      // retorna 3;
+true;     // retorna 1
+false;    // retorna 0
-true;     // retorna -1;

// Operador de exponeciação (**)
2 ** 3;     // retorno 8
10 ** -1;   // retorna 0.1

// Operador de argumento ()
2 * (3 + 2);